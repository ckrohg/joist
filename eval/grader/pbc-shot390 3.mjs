import { chromium } from 'playwright';
const b = await chromium.launch();
const p = await b.newPage();
await p.setViewportSize({ width: 390, height: 900 });
await p.goto(process.argv[2], { waitUntil: 'networkidle', timeout: 60000 });
await p.evaluate(async()=>{const h=document.documentElement.scrollHeight,s=window.innerHeight;for(let y=0;y<h;y+=s){window.scrollTo(0,y);await new Promise(r=>setTimeout(r,120));}window.scrollTo(0,0);});
await p.waitForTimeout(800);
await p.screenshot({ path: process.argv[3], fullPage: true });
console.log('saved', process.argv[3]);
await b.close();
