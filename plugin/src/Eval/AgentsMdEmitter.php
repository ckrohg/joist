<?php
declare(strict_types=1);

namespace Joist\Eval;

/**
 * @purpose v0.9 Wave 10a — render a site's effective preference memory as an
 *          AGENTS.md document for downstream agents (Cursor, Claude Code,
 *          Codex, Aider, Cline) to inherit.
 *
 * Per WAVE_9_2026-05-29.md §3.7 and the cross-tool AGENTS.md standard
 * (https://www.mdfile.exchange/compare/agents-md-vs-cursor-rules), when Joist
 * builds a site it can emit an AGENTS.md the user hands to any other tool;
 * those tools then honor the same per-site brand rules without having to
 * re-learn them.
 *
 * Output shape (target ~3-5 KB, hard cap MAX_BYTES):
 *
 *   # AGENTS.md — <site_id>
 *
 *   <preamble: who/what + how to read this file>
 *
 *   ## Constitution
 *   <site constitution principles when W10b ships; otherwise the Joist
 *    house style preamble>
 *
 *   ## Brand rules (auto-learned)
 *   ### Forbidden phrases
 *   - "phrase" — rationale, confidence
 *   ### Preferred vocabulary
 *   ...
 *
 *   ## Provenance
 *   <site_id, rule count, rendered_at>
 *
 * The emitter intentionally produces a stable, deterministic ordering (by
 * kind then confidence then created_at) so two downstream tools reading the
 * same site at the same time get byte-identical output — useful for cache
 * keys and content-addressed retrieval.
 */
final class AgentsMdEmitter
{
    /** Hard cap on rendered output. Cache-friendly + downstream-friendly. */
    public const MAX_BYTES = 5120;

    /** Soft target — emitter starts trimming below this. */
    public const SOFT_TARGET_BYTES = 3072;

    /** Max rules rendered per kind section before truncation. */
    public const MAX_RULES_PER_KIND = 25;

    public function __construct(private PreferenceMemory $memory) {}

    /**
     * Render the AGENTS.md for the given site id. When $siteId is null,
     * uses PreferenceMemory::siteId() (the current request's site).
     *
     * @return array{markdown:string, bytes:int, rule_count:int, truncated:bool, site_id:string}
     */
    public function render(?string $siteId = null): array
    {
        $siteId = $siteId ?? $this->memory->siteId();
        $rules = $this->memory->listActive($siteId);
        $renderedAt = gmdate('Y-m-d\TH:i:s\Z');

        $sections = [];
        $sections[] = "# AGENTS.md — {$siteId}";
        $sections[] = '';
        $sections[] = '> This file is generated by [Joist](https://joist.dev) — '
            . 'the per-site brand rules captured from the site\'s own edit history. '
            . 'Any agent (Cursor, Claude Code, Codex, Aider, Cline) reading this '
            . 'file SHOULD honor these rules when generating or editing copy and '
            . 'layout for this site.';
        $sections[] = '';
        $sections[] = '_Cross-tool standard: see https://agents.md_';
        $sections[] = '';

        // Constitution slot — W10b is building this in parallel. We render a
        // placeholder + guard so this emitter ships independently.
        $sections[] = '## Constitution';
        $sections[] = '';
        $constitution = $this->renderConstitution($siteId);
        if ($constitution !== '') {
            $sections[] = $constitution;
        } else {
            $sections[] = '_(No site-specific constitution loaded. Default Joist '
                . 'principles apply: avoid stock-photo gradients, prefer concrete '
                . 'language over jargon, write to the buyer not the company.)_';
        }
        $sections[] = '';

        // Brand rules section.
        $sections[] = '## Brand rules (auto-learned)';
        $sections[] = '';
        $sections[] = sprintf(
            '_%d active rule(s) — captured from rejected edits and explicit /remember calls._',
            count($rules)
        );
        $sections[] = '';

        $byKind = [];
        foreach ($rules as $r) {
            $byKind[$r->kind][] = $r;
        }

        $kindLabels = [
            Rule::KIND_FORBIDDEN_PHRASE => 'Forbidden phrases (never write these)',
            Rule::KIND_PREFERRED_VOCAB  => 'Preferred vocabulary',
            Rule::KIND_VOICE_RULE       => 'Brand voice rules',
            Rule::KIND_LAYOUT_PREFERENCE => 'Layout preferences',
            Rule::KIND_COLOR_PREFERENCE => 'Color preferences',
            Rule::KIND_ELEMENT_REFUSED  => 'Refused element types',
            Rule::KIND_STRUCTURAL       => 'Structural preferences',
        ];

        $truncated = false;
        foreach ($kindLabels as $kind => $label) {
            if (empty($byKind[$kind])) continue;

            // Stable sort: confidence DESC, then created_at DESC.
            usort($byKind[$kind], static function (Rule $a, Rule $b): int {
                if ($a->confidence !== $b->confidence) {
                    return $b->confidence <=> $a->confidence;
                }
                return strcmp($b->createdAt, $a->createdAt);
            });

            $section = ["### {$label}", ''];
            $kept = 0;
            foreach ($byKind[$kind] as $rule) {
                if ($kept >= self::MAX_RULES_PER_KIND) {
                    $section[] = sprintf(
                        '- _(+%d more — truncated)_',
                        count($byKind[$kind]) - $kept
                    );
                    $truncated = true;
                    break;
                }
                $section[] = $this->renderRule($rule);
                $kept++;
            }
            $section[] = '';

            $candidate = implode("\n", $sections) . "\n" . implode("\n", $section);
            if (strlen($candidate) > self::MAX_BYTES) {
                $truncated = true;
                $sections[] = '_…remaining rule sections truncated to stay under '
                    . self::MAX_BYTES . ' bytes._';
                break;
            }
            foreach ($section as $line) $sections[] = $line;
        }

        // Footer / provenance.
        $sections[] = '## Provenance';
        $sections[] = '';
        $sections[] = "- **site_id**: `{$siteId}`";
        $sections[] = '- **rule_count**: ' . count($rules);
        $sections[] = "- **rendered_at**: `{$renderedAt}`";
        $sections[] = '- **emitter_version**: `v0.9-wave-10a`';
        $sections[] = '';

        $markdown = implode("\n", $sections);

        // Final hard-cap. If we still overshoot (shouldn't, but be defensive),
        // truncate cleanly on a newline boundary.
        if (strlen($markdown) > self::MAX_BYTES) {
            $truncated = true;
            $markdown = substr($markdown, 0, self::MAX_BYTES);
            $lastNl = strrpos($markdown, "\n");
            if ($lastNl !== false) $markdown = substr($markdown, 0, $lastNl);
            $markdown .= "\n\n_…truncated at " . self::MAX_BYTES . " bytes._\n";
        }

        return [
            'markdown'   => $markdown,
            'bytes'      => strlen($markdown),
            'rule_count' => count($rules),
            'truncated'  => $truncated,
            'site_id'    => $siteId,
        ];
    }

    /**
     * Render the constitution block. W10b (constitution substrate) is being
     * built in parallel; we guard behind class_exists so this emitter ships
     * independently. When the loader is missing, returns '' and the caller
     * substitutes a default preamble.
     */
    private function renderConstitution(string $siteId): string
    {
        $class = '\\Joist\\Eval\\ConstitutionLoader';
        if (!class_exists($class) || !method_exists($class, 'renderForSite')) {
            return '';
        }
        $callable = [$class, 'renderForSite'];
        if (!is_callable($callable)) return '';
        try {
            $rendered = (string) $callable($siteId);
            return trim($rendered);
        } catch (\Throwable) {
            return '';
        }
    }

    /**
     * Render a single rule as a markdown bullet with rationale + confidence.
     * Format: - "pattern" — rationale [conf: 0.85]
     */
    private function renderRule(Rule $rule): string
    {
        $pattern = $this->oneLine($rule->pattern);
        $rationale = $rule->rationale !== null && $rule->rationale !== ''
            ? $this->oneLine($rule->rationale)
            : $this->oneLine($rule->directive);
        $conf = sprintf('%.2f', $rule->confidence);

        // Keep each line under ~250 chars so the AGENTS.md file diff-reviews well.
        $maxLineChars = 220;
        $line = sprintf('- "%s" — %s _[conf: %s]_', $pattern, $rationale, $conf);
        if (strlen($line) > $maxLineChars) {
            $line = substr($line, 0, $maxLineChars - 1) . '…';
        }
        return $line;
    }

    private function oneLine(string $text): string
    {
        $collapsed = preg_replace('/\s+/u', ' ', trim($text)) ?? trim($text);
        // Escape the markdown bullet's own quoting.
        return str_replace('"', '\\"', $collapsed);
    }
}
